@section('script')

    <script>

        let loadImage = document.getElementById('loadImage')
        let imagePreview = document.getElementById('imagePreview');

        let name = document.querySelector('.name');
        let description = document.querySelector('.description');
        let price = document.querySelector('.price');

        name.addEventListener('keyup', e => {
            document.querySelector('.product_name').innerHTML = name.value  
        })

        description.addEventListener('keyup', e => {
            document.querySelector('.product_description').innerHTML = description.value  
        })

        price.addEventListener('keyup', e => {
            document.querySelector('.product_price').innerHTML = '$'+price.value  
        })


        loadImage.addEventListener('change', event => {
            var file = event.target.files[0];

            // Check if a file is selected
            if (file) {
                // Create a FileReader to read the file
                var reader = new FileReader();

                // Set up the FileReader to display the image when loaded
                reader.onload = function (e) {
                    // Set the source of the image element to the loaded data URL
                    imagePreview.src = e.target.result;

                    // Display the image element
                    imagePreview.style.display = 'block';
                };

                // Read the file as a data URL
                reader.readAsDataURL(file);
            } else {
                // No file selected, hide the image element
                imagePreview.style.display = 'none';
            }
        })

    </script>
    
    

